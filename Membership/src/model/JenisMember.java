package model;

public class JenisMember {
    private String Id;
    private String nama;

    public void setId(String Id)
    {
        this.Id = Id;
    }

    public String getId()
    {
        return Id;
    }

    public void setNama(String nama)
    {
        this.nama = nama;
    }
    public String getNama()
    {
        return nama;
    }
}
